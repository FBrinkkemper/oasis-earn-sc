export function getOneInchProtocols(chainId: number) {
    switch (chainId) {
        case 1:
            return ETHEREUM_MAINNET_LIQUIDITY_PROVIDERS.join(',')
        case 42161:
            return ARBITRUM_DEFAULT_LIQUIDITY_PROVIDERS.join(',')
        case 10:
            return OPTIMISM_DEFAULT_PROCOTOLS.join(',')
        case 8453:
            return BASE_DEFAULT_LIQUIDITY_PROVIDERS.join(',')
        default:
            throw new Error(`Chain id ${chainId} not supported`)
    }
}
